package com.devcode.tiposdedatos;


//Los tipos Boxing y Autoboxing sirven para pasar de tipo primitivo a su tipo Wrapper
public class AutoBoxing {


    public static void main(String[] args) {

        Integer valorEnteroWrapper = 3; //aqui el compilador lo toma como un primitivo, BOXING

        int valorEnteroPrimitivo = new Integer(3); //aqui se esta unboxing


    }
}
